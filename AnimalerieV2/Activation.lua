-- Activate Plugin --
------------------------------------------------------------------------------------------
Plugins[pluginName].Load = function()
	notification("Version " .. Plugins[pluginName]:GetVersion() .. " " .. T[ "PluginText" ] .. ", " .. T[ "Loaded" ]);
end
------------------------------------------------------------------------------------------
-- Unload Plugin --
------------------------------------------------------------------------------------------
Plugins[pluginName].Unload = function()	

	SaveSettings();
	notification(T[ "Unactivated" ]);
end